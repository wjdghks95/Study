
public class Number {

	public static void main(String[] args) {
		// Operator
		System.out.println(6 + 2); // 8
		System.out.println(6 - 2); // 4
		System.out.println(6 * 2); // 12
		System.out.println(6 / 2); // 3

		System.out.println(Math.PI); // 3.141592653589793
		System.out.println(Math.abs(-30)); // 30
		System.out.println(Math.floor(Math.PI)); // 내림 
		System.out.println(Math.ceil(Math.PI)); // 올림
		System.out.println(Math.round(Math.PI)); // 반올림
		System.out.println(Math.max(6, 2)); // 6
		System.out.println(Math.min(6, 2)); // 2
		System.out.println(Math.random()*99); // 난수
	}

}
